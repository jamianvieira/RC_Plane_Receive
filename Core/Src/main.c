//our address to receive data on our nrf24 module
uint64_t RxpipeAddrs = 0x11223344AA;
//array to hold our data from nrf24
uint8_t myRxData[4];

//Variables required for smoothing algorithm
const uint8_t len = 8;				//amount of samples
uint32_t arrNumbers[4][8] = {0};    // the readings from the analog input
uint8_t pos = 0;                	// the index of the current reading
uint32_t sum[4] = {0};              // the running total
